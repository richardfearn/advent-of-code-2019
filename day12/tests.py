class Part1Tests(unittest.TestCase):

    def test_calc_gravity(self):
        ganymede = Vector(3, 0, 0)
        callisto = Vector(5, 0, 0)
        self.assertEqual(Vector(1, 0, 0), System.calc_gravity(ganymede, callisto))
        self.assertEqual(Vector(-1, 0, 0), System.calc_gravity(callisto, ganymede))

    def test_example_1(self):
        self.run_example(PART_1_EXAMPLE_1_MOONS_INITIAL, PART_1_EXAMPLE_1_EXPECTED_TEXT, 179)

    def test_example_2(self):
        self.run_example(PART_1_EXAMPLE_2_MOONS_INITIAL, PART_1_EXAMPLE_2_EXPECTED_TEXT, 1940)

    def run_example(self, moons_initial_text, expected_text, expected_final_energy):

        system = parse_text(moons_initial_text)
        expected = parse_expected_text(expected_text)

        steps_to_check = list(expected.keys())

        steps_run_so_far = 0
        for step in steps_to_check:
            while steps_run_so_far < step:
                system = system.run()
                steps_run_so_far += 1
            self.assertEqual(expected[steps_run_so_far], system, "after step %d" % step)

        self.assertEqual(expected_final_energy, system.total_energy())

    def test_with_input(self):
        system = read_input()
        for i in range(1000):
            system = system.run()
        self.assertEqual(12466, system.total_energy())


class Part2Tests(unittest.TestCase):

    def test_example_1(self):
        initial_system = parse_text(PART_1_EXAMPLE_1_MOONS_INITIAL)

        # slow way
        system = initial_system
        for i in range(2772):
            system = system.run()
        self.assertEqual(system, initial_system)

        # fast way
        self.assertEqual(2772, initial_system.steps_to_return_to_same_state())

    def test_example_2(self):
        system = parse_text(PART_1_EXAMPLE_2_MOONS_INITIAL)
        self.assertEqual(4686774924, system.steps_to_return_to_same_state())

    def test_with_input(self):
        system = read_input()
        self.assertEqual(360689156787864, system.steps_to_return_to_same_state())


def parse_expected_text(text):
    lines = text.strip().split("\n")
    expected = chunks(lines, 6)
    expected = [(int(group[0].split(" ")[1]), parse_lines(group[1:5])) for group in expected]
    expected = dict(expected)
    return expected


def chunks(lst, n):
    return [lst[i:i + n] for i in range(0, len(lst), n)]


def read_input():
    return parse_lines(utils.read_input())


def parse_text(text):
    return parse_lines(text.strip().split("\n"))


def parse_lines(lines):
    return System([Moon.from_line(line) for line in lines])


